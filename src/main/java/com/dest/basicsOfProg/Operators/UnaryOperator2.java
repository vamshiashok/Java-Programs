package com.dest.basicsOfProg.Operators;

public class UnaryOperator2 {
	public static void main(String[] args) {
		int a = 37;
		int b = ++a;
		System.out.println(a); // return a = 38
		System.out.println(b);// returns b = 38, here 1st increment operator is there so the a value is incremented and assigned to b.
		
	}
}
