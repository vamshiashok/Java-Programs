package com.dest.JavaPractice.Loops;

public class SampleWhileLoop {
	public static void main(String[] args) {
		/* Syntax:
		 * initialization;
		 * while(condition){
		 * ------
		 * ------ loop logic
		 * ------
		 * increment/decrement;
		 * }
		 * 
		 * Sequence of Execution:
		 * 1. Initialization
		 * 2. Condition
		 * 3. Loop logic
		 * 4. Increment / Decrement
		 */
		
		int i = 1;
		while(i <= 10) {
			System.out.println("Happy Gowri Festival.");
			i++;
		}
	}
}
